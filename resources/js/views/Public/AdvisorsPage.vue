  mounted() {
    document.title = 'Fynla for advisers — help your clients plan better | Fynla';
    const meta = document.querySelector('meta[name="description"]');
    if (meta) {
      meta.setAttribute('content', 'Fynla helps your clients organise their finances before, between, and after your meetings. No product conflicts, no commission — just better-prepared clients.');
    }
    if (this.$route.query.scrollTo) {
      const target = this.$route.query.scrollTo;
      // Ensure page renders at the top before smooth-scrolling down
      window.scrollTo({ top: 0, behavior: 'instant' });
      requestAnimationFrame(() => {
        requestAnimationFrame(() => {
          setTimeout(() => {
            this.smoothScrollTo(target);
          }, 500);
        });
      });
    }
  },

  methods: {
    smoothScrollTo(elementId, duration = 1500) {
      const el = document.getElementById(elementId);
      if (!el) return;
      const target = el.getBoundingClientRect().top + window.scrollY;
      const start = window.scrollY;
      const distance = target - start;
      let startTime = null;

      function easeInOutCubic(t) {
        return t < 0.5 ? 4 * t * t * t : 1 - Math.pow(-2 * t + 2, 3) / 2;
      }

      function step(timestamp) {
        if (!startTime) startTime = timestamp;
        const elapsed = timestamp - startTime;
        const progress = Math.min(elapsed / duration, 1);
        window.scrollTo(0, start + distance * easeInOutCubic(progress));
        if (progress < 1) requestAnimationFrame(step);
      }

      requestAnimationFrame(step);
    },
  },
};
</script>
